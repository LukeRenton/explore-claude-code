#!/bin/bash
# ============================================================
# scripts/ - 실행 가능 코드
# ============================================================
#
# 이 디렉토리는 스킬의 지시사항을 따를 때 Claude가
# 실행할 수 있는 스크립트를 포함합니다. SKILL.md가
# Claude에게 언제 어떻게 실행할지 알려줍니다.
#
# 가이드라인:
#
# - 스크립트는 자체 완결적이거나 상단에 의존성을
#   명확하게 문서화해야 합니다
# - 문제가 발생했을 때 Claude가 복구할 수 있도록
#   유용한 에러 메시지를 포함하세요
# - 엣지 케이스를 우아하게 처리하세요 (없는 파일, 잘못된 입력)
# - 일반적인 언어: Bash, Python, JavaScript, 환경이
#   지원하는 모든 것
#
# Claude가 이것을 사용하는 방법:
#
#   SKILL.md에 다음과 같이 작성할 수 있습니다:
#   "scripts/helper.sh를 실행하여 출력을 검증하세요"
#   Claude가 이 스크립트를 실행하고 결과를 읽습니다.
#
# ${CLAUDE_SKILL_DIR}을 사용하여 스크립트를 참조할 수 있습니다:
#   "${CLAUDE_SKILL_DIR}/scripts/helper.sh를 실행하세요"
#   현재 작업 디렉토리와 관계없이 절대 경로로 해석됩니다.
#
# ============================================================

echo "이것은 플레이스홀더 스크립트입니다."
echo "실제 자동화 코드로 대체하세요."